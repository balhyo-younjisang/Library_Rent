package vo;

public class RentVO {
	private String rent_ymd;
	private String rent_no;
	private String rent_book;
	private int rent_rent;
	private String close_ymd;
	private String return_ymd;
	private String retufn_fg;
	
	public RentVO() {
		
	}
	
	public String getRent_ymd() {
		return rent_ymd;
	}
	public void setRent_ymd(String rent_ymd) {
		this.rent_ymd = rent_ymd;
	}
	public String getRent_no() {
		return rent_no;
	}
	public void setRent_no(String rent_no) {
		this.rent_no = rent_no;
	}
	public String getRent_book() {
		return rent_book;
	}
	public void setRent_book(String rent_book) {
		this.rent_book = rent_book;
	}
	public int getRent_rent() {
		return rent_rent;
	}
	public void setRent_rent(int rent_rent) {
		this.rent_rent = rent_rent;
	}
	public String getClose_ymd() {
		return close_ymd;
	}
	public void setClose_ymd(String close_ymd) {
		this.close_ymd = close_ymd;
	}
	public String getReturn_ymd() {
		return return_ymd;
	}
	public void setReturn_ymd(String return_ymd) {
		this.return_ymd = return_ymd;
	}
	public String getRetufn_fg() {
		return retufn_fg;
	}
	public void setRetufn_fg(String retufn_fg) {
		this.retufn_fg = retufn_fg;
	}
	
}
